"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
#Tipagem Dinâmica infere que o python ja detecta o tipo de dado dentro do argumento da função print(argumento)
print(1234)# <- nesse caso ele ja entende que é um numero inteiro = 1234


# Aspas simples
print('Ronei Fernandes')
print(1, 'Ronei "Fernandes"')

# Aspas duplas
print("Ronei Fernandes")
print(2, "Ronei 'Fernandes'")

# Escape
print("Ronei \"Fernandes\"")

# r
print(r"Ronei \"Fernandes\"")

# Macete, aspas dentro de aspas
print('"Ronei Fernandes"')
